class Multiplication
{
public static void main(String[] args)
{
for(int i=1; i<=10; i++)
{
System.out.println("10*" +i+ "=" +10*i);
}
System.out.println();
int j=1;
while(j<=10)
{
System.out.println("2*" +j+ "=" +2*j);
j++;
}
System.out.println();
int f=1;
do
{
System.out.println("5*" +f+ "=" +5*f);
f++;
}
while(f<=10);
}
}



